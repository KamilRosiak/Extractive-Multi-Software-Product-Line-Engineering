	public static String[] editorLevels = null;
	
	public static final String getLevel(int level) {
		if (MyTreasureConstants.FREE_VERSION) {
			if ((level < 0) || (level >= tutorial.length)) {
				return null;
			}
			return tutorial[level];
		}
		if ((level < 0) || (level >= levels.length)) {
			return null;
		}
		return levels[level];
	}
	
	public static final int MAX_LEVELS = levels.length;
	
	public static boolean isIn(String level) {
		for (int i = 0; i < levels.length; i++) {
			if (level.equals(levels[i])) {
				return true;
			}
		}
		return false;
	}
	
	public static boolean isInWithoutSteps(String level) {
		for (int i = 0; i < levels.length; i++) {
			if (level.substring(0, level.length() - 4).equals(levels[i].substring(0, levels[i].length() - 4))) {
				return true;
			}
		}
		return false;
	}
	
