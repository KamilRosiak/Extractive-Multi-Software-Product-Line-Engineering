package apoSimple.entity;

public class ApoSimpleAnimationHelp {

	private final int image;
	private final int time;
	
	public ApoSimpleAnimationHelp(final int image, final int time) {
		this.image = image;
		this.time = time;
	}

	public int getImage() {
		return this.image;
	}

	public int getTime() {
		return this.time;
	}
	
}
